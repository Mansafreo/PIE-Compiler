#include <math.h>
#include <stdlib.h>

double pie_floor(double x) {
    return floor(x);
}

double pie_ceil(double x) {
    return ceil(x);
}

int pie_rand() {
    return rand();
}

double pie_sqrt(double x) {
    return sqrt(x);
}

double pie_pow(double base, double exp) {
    return pow(base, exp);
}

double pie_sin(double x) {
    return sin(x);
}

double pie_cos(double x) {
    return cos(x);
}
